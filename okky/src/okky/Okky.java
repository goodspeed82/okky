package okky;

public class Okky {
	private int okkyID;
	private String okkyTitle;
	private String userID;
	private String okkyDate;
	private String okkyContent;
	private int okkyAvailable;
	
	public int getOkkyID() {
		return okkyID;
	}
	public void setOkkyId(int okkyId) {
		this.okkyID = okkyId;
	}
	public String getOkkyTitle() {
		return okkyTitle;
	}
	public void setOkkyTitle(String okkyTitle) {
		this.okkyTitle = okkyTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getOkkyDate() {
		return okkyDate;
	}
	public void setOkkyDate(String okkyDate) {
		this.okkyDate = okkyDate;
	}
	public String getOkkyContent() {
		return okkyContent;
	}
	public void setOkkyContent(String okkyContent) {
		this.okkyContent = okkyContent;
	}
	public int getOkkyAvailable() {
		return okkyAvailable;
	}
	public void setOkkyAvailable(int okkyAvailable) {
		this.okkyAvailable = okkyAvailable;
	}
	
	
}
